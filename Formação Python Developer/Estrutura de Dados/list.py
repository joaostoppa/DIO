lista = ["p", "y", "t", "h", "o", "n"]

print (lista[3:]) #['h', 'o', 'n']
print (lista[:5]) #['p', 'y', 't', 'h', 'o']
print (lista[2:6]) #['t', 'h', 'o', 'n']
print (lista[1:5:3]) #['y', 'o']
print (lista[:5:2]) #['p', 't', 'o']
print (lista[::]) #['p', 'y', 't', 'h', 'o', 'n']
print (lista[::-1]) #['n', 'o', 'h', 't', 'y', 'p']

for listas in lista:
    print(listas)




